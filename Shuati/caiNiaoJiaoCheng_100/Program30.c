/*
题目：一个5位数，判断它是不是回文数。即12321是回文数，个位与万位相同，十位与千位相同。

程序分析：学会分解出每一位数。
*/

#include <stdio.h>

int main(void)
{
  long a, b, d, e, x;
  printf("请输入一个五位数：");
  scanf("%ld", &x);
  a = x/10000;        /*分解出万位*/
  b = x/1000 % 10;    /*分解出千位*/
  d = x/10 % 10;      /*分解出十位*/
  e = x % 10;         /*分解出个位*/
  if (a == e && b == d)
    printf("是回文数；");
  else
    printf("不是回文数；");
  return 0;
}